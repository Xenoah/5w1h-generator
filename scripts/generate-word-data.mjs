function family(name, left, right, render) {
  if (left.length !== 30 || right.length !== 10) {
    throw new Error(`${name}: expected 30 x 10 parts, got ${left.length} x ${right.length}`);
  }
  return { name, values: left.flatMap((a) => right.map((b) => render(a, b))) };
}

