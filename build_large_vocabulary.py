def build_unique_combo(prefixes, suffixes, target=TARGET):
    items = []
    seen = set()
    for p in prefixes:
        for s in suffixes:
            candidate = f"{p}{s}"
            if candidate not in seen:
                seen.add(candidate)
                items.append(candidate)
            if len(items) >= target:
                return items
    # fallback for smaller combos
    while len(items) < target:
        p = random.choice(prefixes)
        s = random.choice(suffixes)
        candidate = f"{p}{s}"
        if candidate not in seen:
            seen.add(candidate)
            items.append(candidate)
    return items


